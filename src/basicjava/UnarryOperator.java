public class UnarryOperator {
    public static void main(String[] args) {
        int x = 10;
        int result;
        
        result = ++x;
        System.out.println("resul = "+result);
        
        result = ++x;
        System.out.println("resul = "+result);
        
        result = --x;
        System.out.println("resul = "+result);
        
        result = x--;
        System.out.println("resul = "+result);
        
        result = ++x;
        System.out.println("resul = "+result);
        
        result = ++x;
        System.out.println("resul = "+result);
        
        result = x--;
        System.out.println("resul = "+result);
    }
    
}
